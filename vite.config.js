import { defineConfig, loadEnv } from "vite";
import react from "@vitejs/plugin-react-swc";
import tailwindcss from "@tailwindcss/vite";
import path from "path";

export default defineConfig(({ mode }) => {
  const env = loadEnv(mode, process.cwd(), "");
  
  return {
    plugins: [react(), tailwindcss()],
    resolve: {
      alias: {
        "@": path.resolve(__dirname, "./src"),
      },
    },
    define: {
      "import.meta.env.VITE_API_SECURITY_URL": JSON.stringify(env.VITE_API_SECURITY_URL),
      "import.meta.env.VITE_API_ABMS_URL": JSON.stringify(env.VITE_API_ABMS_URL),
      "import.meta.env.VITE_API_REPORTS_URL": JSON.stringify(env.VITE_API_REPORTS_URL),
    },
  };
});
